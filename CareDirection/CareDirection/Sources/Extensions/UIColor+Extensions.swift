import UIKit

extension UIColor {
    
    @nonobjc class var lightBlueGrey: UIColor {
      return UIColor(red: 181.0 / 255.0, green: 224.0 / 255.0, blue: 228.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var veryLightPink: UIColor {
      return UIColor(white: 205.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var paleSalmon: UIColor {
      return UIColor(red: 1.0, green: 171.0 / 255.0, blue: 171.0 / 255.0, alpha: 1.0)
    }


}
