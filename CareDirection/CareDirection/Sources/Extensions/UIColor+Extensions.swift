import UIKit

extension UIColor {
    
    @nonobjc class var lightBlueGrey: UIColor {
      return UIColor(red: 181.0 / 255.0, green: 224.0 / 255.0, blue: 228.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var veryLightPink: UIColor {
      return UIColor(white: 205.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var paleSalmon: UIColor {
      return UIColor(red: 1.0, green: 171.0 / 255.0, blue: 171.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var white40: UIColor {
         return UIColor(white: 1.0, alpha: 0.4)
    }
       
    @nonobjc class var topaz: UIColor {
        return UIColor(red: 19.0 / 255.0, green: 165.0 / 255.0, blue: 168.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var tealBlue: UIColor {
      return UIColor(red: 0.0, green: 165.0 / 255.0, blue: 168.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var paleCyan: UIColor {
      return UIColor(red: 182.0 / 255.0, green: 1.0, blue: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var lightTan: UIColor {
      return UIColor(red: 235.0 / 255.0, green: 240.0 / 255.0, blue: 176.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownishGrey30: UIColor {
      return UIColor(white: 103.0 / 255.0, alpha: 0.3)
    }
    
    @nonobjc class var tealBlue40: UIColor {
      return UIColor(red: 0.0, green: 165.0 / 255.0, blue: 168.0 / 255.0, alpha: 0.4)
    }


}
